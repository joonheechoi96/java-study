package exercise.p291;

public class Printer {
	
	public int println (int x) {
		int result = x;
		System.out.println(result);
		return x;
	}
	
	public boolean println (boolean x) {
		boolean result = x;
		System.out.println(result);
		return x;
	}
	
	public double println (double x) {
		double result = x;
		System.out.println(result);
		return x;
	}
	
	public String println (String x) {
		String result = x;
		System.out.println(result);
		return x;
	}
}
